package Pipes;

import Filters.Filter;

/**
 *
 * @author Alejandro Lagunes
 */
public class Pipe
{
    // -----------------------------------------------------------------------------------------------------------------
    // -----------------------------------------------------------------------------------------------------------------
    private Filter root;
    // -----------------------------------------------------------------------------------------------------------------
    // -----------------------------------------------------------------------------------------------------------------
    public Object execute(Object entry)
    {
        return this.root.execute(entry);
    }
    // -----------------------------------------------------------------------------------------------------------------
    // -----------------------------------------------------------------------------------------------------------------
    public Object execute(Object entry1, Object entry2)
    {
        return this.root.execute(entry1,entry2);
    }
    // -----------------------------------------------------------------------------------------------------------------
    // -----------------------------------------------------------------------------------------------------------------
    public Pipe register(Filter filter)
    {
        if (this.root == null)
        {
            this.root = filter;
        }
        else
        {
            this.root.register(filter);
        }
        return this;
    }
    // -----------------------------------------------------------------------------------------------------------------
    // -----------------------------------------------------------------------------------------------------------------
}
