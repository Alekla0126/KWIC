package Filters;

import java.util.ArrayList;


/**
 *
 * @author Alejandro Lagunes
 */
public class LowerCaseFilter extends Filter
{
    // -----------------------------------------------------------------------------------------------------------------
    // -----------------------------------------------------------------------------------------------------------------
    @Override
    protected Object process(Object entry)
    {
        // -------------------------------------------------------------------------------------------------------------
        ArrayList<String> words = (ArrayList) entry;
        int index = 0;
        // -------------------------------------------------------------------------------------------------------------
        while(index < words.size())
        {
            words.set(index, words.get(index).toLowerCase());
            index++;
        }
        // -------------------------------------------------------------------------------------------------------------
        return words;
    }
    // -----------------------------------------------------------------------------------------------------------------
    // -----------------------------------------------------------------------------------------------------------------
    @Override
    protected Object process(Object entry1, Object entry2)
    {
        return null;
    }
    // -----------------------------------------------------------------------------------------------------------------
    // -----------------------------------------------------------------------------------------------------------------
}
