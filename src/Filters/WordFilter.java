package Filters;

import java.util.ArrayList;
import java.util.Arrays;

/**
 *
 * @author Alejandro Lagunes
 */
public class WordFilter extends Filter
{
    // -----------------------------------------------------------------------------------------------------------------
    // -----------------------------------------------------------------------------------------------------------------
    @Override
    protected Object process(Object entry)
    {
        ArrayList<String> lines = (ArrayList) entry;
        ArrayList<String> words = new ArrayList<>();
        int index = 0;
        // -------------------------------------------------------------------------------------------------------------
        while(index < lines.size())
        {
            String[] word;
            word = lines.get(index).split(" ");
            words.addAll(Arrays.asList(word));
            index++;
        }
        // -------------------------------------------------------------------------------------------------------------
        return words;
    }
    // -----------------------------------------------------------------------------------------------------------------
    // -----------------------------------------------------------------------------------------------------------------
    @Override
    protected Object process(Object entry1, Object entry2)
    {
        return null;
    }
    // -----------------------------------------------------------------------------------------------------------------
    // -----------------------------------------------------------------------------------------------------------------
}
